package com.cg.fds.dto.restaurant;

import javax.validation.constraints.NotBlank;
import javax.validation.constraints.Size;

public class AddRestaurantRequest {
	
	@NotBlank(message="Restaurant name cannot be null")
	private String restaurantName;
	private String managerName;
	
	@Size(min=10,max=10,message="Contact length for the restaurant should be equal to 10")
	private String contactNumber;
	private String buildingName;
	@NotBlank(message="Restaurant area cannot be null")
	private String area;
	private String streetNo;
	private String city;
	private String state;
	private String country;
	private String pincode;

	public AddRestaurantRequest() {
	}

	public String getRestaurantName() {
		return restaurantName;
	}

	public void setRestaurantName(String restaurantName) {
		this.restaurantName = restaurantName;
	}

	public String getManagerName() {
		return managerName;
	}

	public void setManagerName(String managerName) {
		this.managerName = managerName;
	}

	public String getContactNumber() {
		return contactNumber;
	}

	public void setContactNumber(String contactNumber) {
		this.contactNumber = contactNumber;
	}

	public String getBuildingName() {
		return buildingName;
	}

	public void setBuildingName(String buildingName) {
		this.buildingName = buildingName;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

	public String getStreetNo() {
		return streetNo;
	}

	public void setStreetNo(String streetNo) {
		this.streetNo = streetNo;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getPincode() {
		return pincode;
	}

	public void setPincode(String pincode) {
		this.pincode = pincode;
	}

}
